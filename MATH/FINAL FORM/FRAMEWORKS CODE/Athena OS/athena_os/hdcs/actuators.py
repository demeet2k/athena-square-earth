class ModulatedActuator(ActuatorOperator):
    """
    Actuator with time-dependent modulation.
    
    Control signal: u(t) = A(t) × carrier(t)
    
    Where carrier can be constant, harmonic, pulse, or optimal.
    """
    
    def __init__(self, dimension: int,
                 modulation_type: ModulationType = ModulationType.HARMONIC,
                 **kwargs):
        super().__init__(dimension, **kwargs)
        
        self.modulation_type = modulation_type
        
        # Modulation parameters
        self._amplitude = 1.0
        self._frequency = 1.0
        self._phase = 0.0
        self._pulse_width = 1.0
    
    def carrier(self, t: float) -> float:
        """Generate carrier signal."""
        if self.modulation_type == ModulationType.CONSTANT:
            return 1.0
        
        elif self.modulation_type == ModulationType.HARMONIC:
            return np.cos(self._frequency * t + self._phase)
        
        elif self.modulation_type == ModulationType.PULSE:
            period = 2 * np.pi / self._frequency
            t_mod = t % period
            return 1.0 if t_mod < self._pulse_width else 0.0
        
        elif self.modulation_type == ModulationType.CHIRP:
            # Linear chirp
            instantaneous_freq = self._frequency * (1 + t)
            return np.cos(instantaneous_freq * t + self._phase)
        
        return 1.0
    
    def modulated_control(self, base_control: np.ndarray, t: float) -> np.ndarray:
        """
        Apply modulation to base control signal.
        
        u(t) = A × base × carrier(t)
        """
        return self._amplitude * base_control * self.carrier(t)
    
    def apply_modulated_control(self, state: np.ndarray,
                                base_control: np.ndarray,
                                t: float, dt: float) -> np.ndarray:
        """Apply modulated control to state."""
        modulated = self.modulated_control(base_control, t)
        return self.apply_control(state, modulated, t, dt)
    
    def set_modulation_params(self, amplitude: float = None,
                              frequency: float = None,
                              phase: float = None,
                              pulse_width: float = None) -> None:
        """Set modulation parameters."""
        if amplitude is not None:
            self._amplitude = amplitude
        if frequency is not None:
            self._frequency = frequency
        if phase is not None:
            self._phase = phase
        if pulse_width is not None:
            self._pulse_width = pulse_width

