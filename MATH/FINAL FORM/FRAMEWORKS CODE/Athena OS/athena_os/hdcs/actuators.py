class GeneratorOperator:
    """
    Generator Operator Ĝ_k.
    
    Elements of the Lie algebra generating the dynamical group.
    Control Hamiltonian: Ĥ_ctrl = Σ u_k(t) Ĝ_k
    """
    
    def __init__(self, dimension: int, generator_type: str = "position"):
        self.dimension = dimension
        self.generator_type = generator_type
        
        # Generator matrix
        self._matrix = self._create_generator()
    
    def _create_generator(self) -> np.ndarray:
        """Create generator matrix based on type."""
        n = self.dimension
        
        if self.generator_type == "position":
            # Position operator (diagonal)
            return np.diag(np.arange(n) - n/2)
        
        elif self.generator_type == "momentum":
            # Momentum operator (tridiagonal)
            G = np.zeros((n, n), dtype=complex)
            for i in range(n - 1):
                G[i, i+1] = -1j * np.sqrt(i+1)
                G[i+1, i] = 1j * np.sqrt(i+1)
            return G
        
        elif self.generator_type == "number":
            # Number operator
            return np.diag(np.arange(n))
        
        elif self.generator_type == "raising":
            # Raising operator a†
            G = np.zeros((n, n), dtype=complex)
            for i in range(n - 1):
                G[i+1, i] = np.sqrt(i+1)
            return G
        
        elif self.generator_type == "lowering":
            # Lowering operator a
            G = np.zeros((n, n), dtype=complex)
            for i in range(n - 1):
                G[i, i+1] = np.sqrt(i+1)
            return G
        
        elif self.generator_type == "phase":
            # Phase operator
            return np.diag(np.exp(1j * 2 * np.pi * np.arange(n) / n))
        
        elif self.generator_type == "sigma_x":
            # Pauli X (if 2D)
            if n == 2:
                return np.array([[0, 1], [1, 0]], dtype=complex)
            return np.eye(n)
        
        elif self.generator_type == "sigma_y":
            # Pauli Y (if 2D)
            if n == 2:
                return np.array([[0, -1j], [1j, 0]], dtype=complex)
            return np.eye(n)
        
        elif self.generator_type == "sigma_z":
            # Pauli Z (if 2D)
            if n == 2:
                return np.array([[1, 0], [0, -1]], dtype=complex)
            return np.diag([1, -1] + [0]*(n-2))
        
        return np.eye(n)
    
    def apply(self, state: np.ndarray, coefficient: float = 1.0) -> np.ndarray:
        """
        Apply generator to state.
        
        Ĝ|Ψ⟩ multiplied by coefficient
        """
        return coefficient * self._matrix @ state
    
    def commutator(self, other: 'GeneratorOperator') -> np.ndarray:
        """
        Compute commutator [Ĝ₁, Ĝ₂] = Ĝ₁Ĝ₂ - Ĝ₂Ĝ₁
        """
        return self._matrix @ other._matrix - other._matrix @ self._matrix
    
    def is_hermitian(self) -> bool:
        """Check if generator is Hermitian."""
        return np.allclose(self._matrix, self._matrix.conj().T)
    
    @property
    def matrix(self) -> np.ndarray:
        return self._matrix.copy()

