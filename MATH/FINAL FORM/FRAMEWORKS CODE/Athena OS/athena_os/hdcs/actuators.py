class ControlHamiltonian:
    """
    Control Hamiltonian Ĥ_ctrl(t).
    
    Ĥ_ctrl(t) = Σ_k u_k(t) Ĝ_k
    
    Combines control signals with generator operators
    to modulate system dynamics.
    """
    
    def __init__(self, dimension: int):
        self.dimension = dimension
        
        # Generators and their control signals
        self._generators: List[GeneratorOperator] = []
        self._control_signals: List[Callable[[float], float]] = []
        
        # Current Hamiltonian matrix
        self._matrix = np.zeros((dimension, dimension), dtype=complex)
    
    def add_control_channel(self, generator: GeneratorOperator,
                           control_signal: Callable[[float], float] = None) -> None:
        """
        Add a control channel.
        
        Ĥ_ctrl += u(t) Ĝ
        """
        self._generators.append(generator)
        
        if control_signal is None:
            control_signal = lambda t: 0.0
        
        self._control_signals.append(control_signal)
    
    def evaluate(self, t: float, control_values: np.ndarray = None) -> np.ndarray:
        """
        Evaluate control Hamiltonian at time t.
        
        Ĥ_ctrl(t) = Σ_k u_k(t) Ĝ_k
        """
        self._matrix = np.zeros((self.dimension, self.dimension), dtype=complex)
        
        for k, (gen, signal) in enumerate(zip(self._generators, self._control_signals)):
            if control_values is not None and k < len(control_values):
                u_k = control_values[k]
            else:
                u_k = signal(t)
            
            self._matrix += u_k * gen.matrix
        
        return self._matrix.copy()
    
    def propagator(self, t: float, dt: float,
                   control_values: np.ndarray = None) -> np.ndarray:
        """
        Compute unitary propagator.
        
        U(dt) = exp(-i Ĥ_ctrl dt / ℏ)
        """
        H = self.evaluate(t, control_values)
        return linalg.expm(-1j * H * dt)
    
    def evolve_state(self, state: np.ndarray, t: float, dt: float,
                     control_values: np.ndarray = None) -> np.ndarray:
        """
        Evolve state under control Hamiltonian.
        
        |Ψ(t+dt)⟩ = U(dt)|Ψ(t)⟩
        """
        U = self.propagator(t, dt, control_values)
        return U @ state
    
    @property
    def num_channels(self) -> int:
        return len(self._generators)
    
    @property
    def matrix(self) -> np.ndarray:
        return self._matrix.copy()

