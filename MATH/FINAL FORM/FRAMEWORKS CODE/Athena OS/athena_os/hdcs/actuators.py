class OptimalActuator(ActuatorOperator):
    """
    Actuator with optimal control pulse design.
    
    Uses GRAPE (Gradient Ascent Pulse Engineering) or
    similar methods to find optimal control sequence.
    """
    
    def __init__(self, dimension: int, target_state: np.ndarray = None,
                 **kwargs):
        super().__init__(dimension, **kwargs)
        
        self._target = target_state
        self._optimal_pulses: List[np.ndarray] = []
        self._pulse_times: np.ndarray = None
    
    def set_target(self, target_state: np.ndarray) -> None:
        """Set target state for optimization."""
        self._target = target_state / np.linalg.norm(target_state)
    
    def fidelity(self, final_state: np.ndarray) -> float:
        """
        Compute fidelity with target.
        
        F = |⟨target|final⟩|²
        """
        if self._target is None:
            return 0.0
        
        overlap = np.abs(np.conj(self._target) @ final_state) ** 2
        return float(overlap)
    
    def optimize_pulse(self, initial_state: np.ndarray,
                       total_time: float, num_steps: int = 100,
                       iterations: int = 100) -> Dict:
        """
        Optimize control pulse using gradient ascent.
        
        Simple GRAPE implementation.
        """
        if self._target is None:
            raise ValueError("Target state not set")
        
        dt = total_time / num_steps
        self._pulse_times = np.linspace(0, total_time, num_steps)
        
        # Initialize random pulses
        num_channels = self.H_ctrl.num_channels
        pulses = 0.1 * np.random.randn(num_steps, num_channels)
        
        # Learning rate
        lr = 0.1
        
        best_fidelity = 0.0
        best_pulses = pulses.copy()
        
        for iteration in range(iterations):
            # Forward propagation
            states = [initial_state.copy()]
            state = initial_state.copy()
            
            for step in range(num_steps):
                state = self.apply_control(state, pulses[step], 
                                          self._pulse_times[step], dt)
                states.append(state.copy())
            
            # Compute fidelity
            fidelity = self.fidelity(state)
            
            if fidelity > best_fidelity:
                best_fidelity = fidelity
                best_pulses = pulses.copy()
            
            # Gradient computation (finite differences)
            gradients = np.zeros_like(pulses)
            eps = 1e-6
            
            for step in range(num_steps):
                for ch in range(num_channels):
                    pulses_plus = pulses.copy()
                    pulses_plus[step, ch] += eps
                    
                    # Propagate with perturbed pulse
                    state_plus = initial_state.copy()
                    for s in range(num_steps):
                        state_plus = self.apply_control(
                            state_plus, pulses_plus[s],
                            self._pulse_times[s], dt)
                    
                    fid_plus = self.fidelity(state_plus)
                    gradients[step, ch] = (fid_plus - fidelity) / eps
            
            # Update pulses
            pulses += lr * gradients
            
            # Clip pulses
            pulses = np.clip(pulses, -1.0, 1.0)
        
        self._optimal_pulses = [best_pulses[i] for i in range(num_steps)]
        
        return {
            "fidelity": best_fidelity,
            "iterations": iterations,
            "num_steps": num_steps,
            "pulses": best_pulses
        }
    
    def get_optimal_control(self, t: float) -> np.ndarray:
        """Get optimal control at time t."""
        if self._pulse_times is None or len(self._optimal_pulses) == 0:
            return np.zeros(self.H_ctrl.num_channels)
        
        # Find nearest time index
        idx = np.searchsorted(self._pulse_times, t)
        idx = min(idx, len(self._optimal_pulses) - 1)
        
        return self._optimal_pulses[idx]

