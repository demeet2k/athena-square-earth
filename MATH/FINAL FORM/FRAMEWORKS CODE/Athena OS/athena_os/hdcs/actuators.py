def validate_actuators() -> bool:
    """Validate actuators module."""
    
    # Test GeneratorOperator
    gen_pos = GeneratorOperator(4, "position")
    gen_mom = GeneratorOperator(4, "momentum")
    
    assert gen_pos.is_hermitian()
    
    comm = gen_pos.commutator(gen_mom)
    assert comm.shape == (4, 4)
    
    # Test ControlHamiltonian
    H_ctrl = ControlHamiltonian(4)
    H_ctrl.add_control_channel(gen_pos)
    H_ctrl.add_control_channel(gen_mom)
    
    H = H_ctrl.evaluate(t=0.0, control_values=np.array([1.0, 0.5]))
    assert H.shape == (4, 4)
    
    U = H_ctrl.propagator(t=0.0, dt=0.1, control_values=np.array([1.0, 0.5]))
    assert U.shape == (4, 4)
    
    # Check unitarity
    assert np.allclose(U @ U.conj().T, np.eye(4), atol=1e-10)
    
    # Test ActuatorOperator
    actuator = ActuatorOperator(4, ActuatorType.UNITARY)
    
    state = np.array([1, 0, 0, 0], dtype=complex)
    state = state / np.linalg.norm(state)
    
    control = np.array([0.1, 0.2, 0.3])
    new_state = actuator.apply_control(state, control, t=0.0, dt=0.1)
    
    # Check normalization preserved
    assert np.abs(np.linalg.norm(new_state) - 1.0) < 1e-10
    
    # Test ModulatedActuator
    mod_actuator = ModulatedActuator(4, ModulationType.HARMONIC)
    mod_actuator.set_modulation_params(amplitude=0.5, frequency=2.0)
    
    carrier = mod_actuator.carrier(0.0)
    assert isinstance(carrier, float)
    
    modulated = mod_actuator.modulated_control(np.array([1.0, 1.0, 1.0]), t=0.0)
    assert len(modulated) == 3
    
    # Test OptimalActuator
    opt_actuator = OptimalActuator(2)
    
    initial = np.array([1, 0], dtype=complex)
    target = np.array([0, 1], dtype=complex)
    
    opt_actuator.set_target(target)
    
    fid = opt_actuator.fidelity(initial)
    assert 0 <= fid <= 1
    
    return True

