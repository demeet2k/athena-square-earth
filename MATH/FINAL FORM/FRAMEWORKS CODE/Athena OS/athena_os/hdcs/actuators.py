class ActuatorOperator:
    """
    The Actuator Operator Â.
    
    Translates classical control signal u(t) into quantum
    mechanical operator Ĥ_ctrl(t).
    
    Physical Implementation:
    - Phase Correction: If error is phase lag Δφ, apply V̂ = u(t)n̂
    - Trajectory Correction: If error is spatial drift Δx, apply P̂ = u(t)p̂
    """
    
    def __init__(self, dimension: int,
                 actuator_type: ActuatorType = ActuatorType.UNITARY):
        self.dimension = dimension
        self.actuator_type = actuator_type
        
        # Control Hamiltonian
        self.H_ctrl = ControlHamiltonian(dimension)
        
        # System Hamiltonian (to be combined)
        self._H_sys = np.zeros((dimension, dimension), dtype=complex)
        
        # Coupling efficiency
        self._coupling = 1.0
        
        # Initialize based on type
        self._setup_actuator()
    
    def _setup_actuator(self) -> None:
        """Set up actuator based on type."""
        if self.actuator_type == ActuatorType.PHASE:
            gen = GeneratorOperator(self.dimension, "number")
            self.H_ctrl.add_control_channel(gen)
        
        elif self.actuator_type == ActuatorType.MOMENTUM:
            gen = GeneratorOperator(self.dimension, "momentum")
            self.H_ctrl.add_control_channel(gen)
        
        elif self.actuator_type == ActuatorType.POSITION:
            gen = GeneratorOperator(self.dimension, "position")
            self.H_ctrl.add_control_channel(gen)
        
        elif self.actuator_type == ActuatorType.AMPLITUDE:
            gen_raise = GeneratorOperator(self.dimension, "raising")
            gen_lower = GeneratorOperator(self.dimension, "lowering")
            self.H_ctrl.add_control_channel(gen_raise)
            self.H_ctrl.add_control_channel(gen_lower)
        
        elif self.actuator_type == ActuatorType.UNITARY:
            # General: position, momentum, and number
            for gen_type in ["position", "momentum", "number"]:
                gen = GeneratorOperator(self.dimension, gen_type)
                self.H_ctrl.add_control_channel(gen)
    
    def set_system_hamiltonian(self, H_sys: np.ndarray) -> None:
        """Set the base system Hamiltonian."""
        self._H_sys = H_sys.copy()
    
    def total_hamiltonian(self, t: float,
                         control: np.ndarray = None) -> np.ndarray:
        """
        Compute total Hamiltonian.
        
        Ĥ_total(t) = Ĥ_sys + Ĥ_ctrl(t)
        """
        H_ctrl = self.H_ctrl.evaluate(t, control)
        return self._H_sys + self._coupling * H_ctrl
    
    def apply_control(self, state: np.ndarray, control: np.ndarray,
                      t: float, dt: float) -> np.ndarray:
        """
        Apply control to state.
        
        |Ψ(t+dt)⟩ = exp(-i Ĥ_total dt)|Ψ(t)⟩
        """
        H_total = self.total_hamiltonian(t, control)
        U = linalg.expm(-1j * H_total * dt)
        return U @ state
    
    def force(self, control: np.ndarray, t: float) -> np.ndarray:
        """
        Compute "force" from control signal.
        
        Force is related to generator expectation values.
        """
        H_ctrl = self.H_ctrl.evaluate(t, control)
        
        # Force magnitude related to Hamiltonian norm
        return np.linalg.norm(H_ctrl) * np.ones(len(control))
    
    def set_coupling(self, coupling: float) -> None:
        """Set actuator coupling efficiency."""
        self._coupling = coupling
    
    def add_custom_channel(self, generator_type: str) -> None:
        """Add custom control channel."""
        gen = GeneratorOperator(self.dimension, generator_type)
        self.H_ctrl.add_control_channel(gen)

