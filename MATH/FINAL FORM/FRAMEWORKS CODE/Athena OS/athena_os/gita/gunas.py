@dataclass
class TamasOperator:
    """
    Tamas (T̂) - The Laplacian Operator (∇²).
    
    Represents divergence of gradient - second derivative of field.
    Governs curvature, density, and localization.
    
    Properties:
        1. Mass generation: ∇²Φ ∝ ρ (density)
        2. Diffusion: smoothes extrema (creates Avidyā)
        3. Potential storage: stores energy as U (Vāsanās)
    
    Computational: save_to_disk, sleep(), buffer_overflow
    """
    
    dimension: int = 4
    _matrix: np.ndarray = field(default=None, repr=False)
    
    def __post_init__(self):
        if self._matrix is None:
            # Laplacian-like matrix (second derivative)
            # Discrete Laplacian: [1, -2, 1] stencil
            self._matrix = np.zeros((self.dimension, self.dimension), dtype=complex)
            for i in range(self.dimension):
                self._matrix[i, i] = -2
                if i > 0:
                    self._matrix[i, i-1] = 1
                if i < self.dimension - 1:
                    self._matrix[i, i+1] = 1
    
    def apply(self, psi: np.ndarray) -> np.ndarray:
        """Apply Tamas operator to state."""
        result = self._matrix @ psi[:self.dimension]
        return result
    
    def laplacian_3d(self, scalar_field: np.ndarray) -> np.ndarray:
        """
        Apply Laplacian to 3D scalar field.
        
        ∇²φ = ∂²φ/∂x² + ∂²φ/∂y² + ∂²φ/∂z²
        """
        return laplace(scalar_field)
    
    def mass_density(self, potential: np.ndarray, 
                     coupling: float = 1.0) -> np.ndarray:
        """
        Compute mass density from potential.
        
        ∇²Φ = 4πGρ (Poisson equation)
        """
        return self.laplacian_3d(potential) / (4 * np.pi * coupling)
    
    def diffuse(self, field: np.ndarray, alpha: float = 0.1, 
                steps: int = 1) -> np.ndarray:
        """
        Apply diffusion (heat equation).
        
        ∂u/∂t = α∇²u
        
        This creates Avidyā (ignorance) by blurring distinctions.
        """
        result = field.copy()
        for _ in range(steps):
            lap = self.laplacian_3d(result) if result.ndim >= 3 else self.apply(result)
            result = result + alpha * lap
        return result
    
    def store_vasana(self, weight: float, memory: np.ndarray) -> np.ndarray:
        """
        Store a Vāsanā (latent impression) in memory.
        
        Tamas acts as the "Hard Drive" (Citta).
        """
        # Add weight to memory matrix
        perturbation = weight * np.ones_like(memory)
        return memory + perturbation
    
    @property
    def gurutva(self) -> str:
        """Heaviness/Mass property."""
        return "gurutva"  # Weight, inertia

