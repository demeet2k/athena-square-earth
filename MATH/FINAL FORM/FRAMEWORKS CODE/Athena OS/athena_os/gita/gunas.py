@dataclass
class RajasOperator:
    """
    Rajas (R̂) - The Gradient Operator (∇).
    
    Maps scalar potential field φ(x) into vector force field F(x).
    Generates linear displacement and velocity.
    
    Properties:
        1. Kinetic injection: introduces T = p²/2m
        2. Positive divergence: ∇·V > 0 (source of flux)
        3. Directionality: imposes specific vector direction
    
    Computational: while(loop), thread.start(), process_interrupt
    """
    
    dimension: int = 4
    _matrix: np.ndarray = field(default=None, repr=False)
    
    def __post_init__(self):
        if self._matrix is None:
            # Derivative-like matrix (gradient in discrete setting)
            # Forward difference operator
            self._matrix = np.zeros((self.dimension, self.dimension), dtype=complex)
            for i in range(self.dimension - 1):
                self._matrix[i, i] = -1
                self._matrix[i, i+1] = 1
    
    def apply(self, psi: np.ndarray) -> np.ndarray:
        """Apply Rajas operator to state."""
        result = self._matrix @ psi[:self.dimension]
        return result
    
    def gradient_3d(self, scalar_field: np.ndarray) -> np.ndarray:
        """
        Apply gradient to 3D scalar field.
        
        ∇φ = (∂φ/∂x, ∂φ/∂y, ∂φ/∂z)
        """
        grad = np.zeros((*scalar_field.shape, 3))
        grad[..., 0] = np.gradient(scalar_field, axis=0)
        grad[..., 1] = np.gradient(scalar_field, axis=1)
        grad[..., 2] = np.gradient(scalar_field, axis=2)
        return grad
    
    def kinetic_energy(self, momentum: np.ndarray, mass: float = 1.0) -> float:
        """Compute kinetic energy T = p²/2m."""
        p_squared = np.sum(np.abs(momentum)**2)
        return float(p_squared / (2 * mass))
    
    def divergence(self, vector_field: np.ndarray) -> np.ndarray:
        """
        Compute divergence ∇·F.
        
        Positive divergence indicates source (Vikṣepa-Śakti).
        """
        div = np.zeros(vector_field.shape[:-1])
        for i in range(vector_field.shape[-1]):
            div += np.gradient(vector_field[..., i], axis=i)
        return div
    
    def sankalpa(self, direction: np.ndarray) -> np.ndarray:
        """
        Impose intention/direction vector.
        
        The power of "desire" or "will".
        """
        # Normalize to unit vector
        norm = np.linalg.norm(direction)
        if norm > 0:
            return direction / norm
        return direction
    
    @property
    def pravritta(self) -> str:
        """Activity/Projection property."""
        return "pravṛtta"  # Outward-moving, active

