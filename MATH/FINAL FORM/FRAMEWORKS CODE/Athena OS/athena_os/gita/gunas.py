@dataclass
class SattvaOperator:
    """
    Sattva (Ŝ) - The Curl Operator (∇×).
    
    Properties:
        1. Divergence-free: ∇·(∇×v) = 0 (no energy leak)
        2. Workless force: W = ∫F·dl = 0 (effortless action)
        3. Harmonic generation: creates frequency and light
    
    Computational: Optimization, efficient processing, return true
    """
    
    # Operator matrix (for discrete states)
    dimension: int = 4
    _matrix: np.ndarray = field(default=None, repr=False)
    
    def __post_init__(self):
        if self._matrix is None:
            # Rotation-like matrix (curl in discrete setting)
            # Anti-symmetric generator
            self._matrix = np.zeros((self.dimension, self.dimension), dtype=complex)
            for i in range(self.dimension - 1):
                self._matrix[i, i+1] = 1j
                self._matrix[i+1, i] = -1j
    
    def apply(self, psi: np.ndarray) -> np.ndarray:
        """Apply Sattva operator to state."""
        result = self._matrix @ psi[:self.dimension]
        return result
    
    def curl_3d(self, vector_field: np.ndarray) -> np.ndarray:
        """
        Apply curl to 3D vector field.
        
        ∇×F = (∂Fz/∂y - ∂Fy/∂z, 
               ∂Fx/∂z - ∂Fz/∂x, 
               ∂Fy/∂x - ∂Fx/∂y)
        """
        # Assuming vector_field has shape (nx, ny, nz, 3)
        Fx, Fy, Fz = vector_field[..., 0], vector_field[..., 1], vector_field[..., 2]
        
        # Compute derivatives using finite differences
        curl = np.zeros_like(vector_field)
        
        # ∂Fz/∂y - ∂Fy/∂z
        curl[..., 0] = np.gradient(Fz, axis=1) - np.gradient(Fy, axis=2)
        # ∂Fx/∂z - ∂Fz/∂x
        curl[..., 1] = np.gradient(Fx, axis=2) - np.gradient(Fz, axis=0)
        # ∂Fy/∂x - ∂Fx/∂y
        curl[..., 2] = np.gradient(Fy, axis=0) - np.gradient(Fx, axis=1)
        
        return curl
    
    @property
    def is_divergence_free(self) -> bool:
        """Verify ∇·(∇×) = 0 property."""
        # For discrete matrix: trace of skew-symmetric = 0
        return abs(np.trace(self._matrix)) < 1e-10
    
    def generate_frequency(self, omega: float, t: float) -> complex:
        """Generate harmonic oscillation e^(iωt)."""
        return np.exp(1j * omega * t)
    
    @property
    def prakasha(self) -> str:
        """Light/Illumination property."""
        return "prakāśa"  # Clarity, luminosity

