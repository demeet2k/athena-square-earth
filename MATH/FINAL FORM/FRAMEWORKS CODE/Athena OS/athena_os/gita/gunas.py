# THE PANCHĪKARAṆA MATRIX
# =============================================================================

def panchakarana_matrix() -> np.ndarray:
    """
    The Pañcīkaraṇa (Quintuplication) Matrix.
    
    Π[i,j] = 0.5 if i==j else 0.125
    
    Each element is 50% dominant + 12.5% of each other.
    """
    Pi = np.ones((5, 5)) * 0.125
    np.fill_diagonal(Pi, 0.5)
    return Pi

