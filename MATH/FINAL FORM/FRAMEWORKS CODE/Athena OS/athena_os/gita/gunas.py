@dataclass
class UniversalHamiltonian:
    """
    The Universal Hamiltonian of Prakṛti.
    
    Ĥ_Prakṛti = α(t)Ŝ + β(t)R̂ + γ(t)T̂
    
    Where the coefficients α, β, γ determine the local phase
    of the simulation (e.g., Satya Yuga vs. Kali Yuga).
    """
    
    dimension: int = 4
    
    # The three operators
    sattva: SattvaOperator = field(default=None)
    rajas: RajasOperator = field(default=None)
    tamas: TamasOperator = field(default=None)
    
    # Time-dependent coefficients
    alpha: float = 1.0  # Sattva coefficient
    beta: float = 1.0   # Rajas coefficient
    gamma: float = 1.0  # Tamas coefficient
    
    def __post_init__(self):
        if self.sattva is None:
            self.sattva = SattvaOperator(self.dimension)
        if self.rajas is None:
            self.rajas = RajasOperator(self.dimension)
        if self.tamas is None:
            self.tamas = TamasOperator(self.dimension)
    
    @property
    def hamiltonian_matrix(self) -> np.ndarray:
        """
        Construct the full Hamiltonian matrix.
        
        H = αS + βR + γT
        """
        H = (self.alpha * self.sattva._matrix +
             self.beta * self.rajas._matrix +
             self.gamma * self.tamas._matrix)
        return H
    
    def set_yuga_coefficients(self, yuga: str) -> None:
        """
        Set coefficients based on Yuga.
        
        Satya: High Sattva (4), Low Tamas (1)
        Kali: Low Sattva (1), High Tamas (4)
        """
        yuga_config = {
            "SATYA": (4.0, 1.0, 1.0),    # S=4, R=1, T=1
            "TRETA": (3.0, 2.0, 1.0),    # S=3, R=2, T=1
            "DVAPARA": (2.0, 2.0, 2.0),  # S=2, R=2, T=2
            "KALI": (1.0, 2.0, 4.0),     # S=1, R=2, T=4
        }
        
        if yuga in yuga_config:
            self.alpha, self.beta, self.gamma = yuga_config[yuga]
    
    def time_evolution_operator(self, dt: float) -> np.ndarray:
        """
        Compute time evolution operator U(dt) = exp(-iHdt).
        """
        H = self.hamiltonian_matrix
        return expm(-1j * H * dt)
    
    def evolve_state(self, psi: np.ndarray, dt: float) -> np.ndarray:
        """
        Evolve state by time dt.
        
        |ψ(t+dt)⟩ = U(dt)|ψ(t)⟩
        """
        U = self.time_evolution_operator(dt)
        evolved = U @ psi[:self.dimension]
        
        # Renormalize
        norm = np.linalg.norm(evolved)
        if norm > 0:
            evolved /= norm
        
        return evolved
    
    def expectation_value(self, psi: np.ndarray) -> Dict[str, float]:
        """
        Compute expectation values of each Guṇa.
        
        ⟨ψ|Ŝ|ψ⟩, ⟨ψ|R̂|ψ⟩, ⟨ψ|T̂|ψ⟩
        """
        psi_trunc = psi[:self.dimension]
        
        exp_S = np.real(np.vdot(psi_trunc, self.sattva._matrix @ psi_trunc))
        exp_R = np.real(np.vdot(psi_trunc, self.rajas._matrix @ psi_trunc))
        exp_T = np.real(np.vdot(psi_trunc, self.tamas._matrix @ psi_trunc))
        
        return {
            "sattva": float(exp_S),
            "rajas": float(exp_R),
            "tamas": float(exp_T),
        }
    
    def dominant_guna(self, psi: np.ndarray) -> GunaType:
        """Determine the dominant Guṇa for a state."""
        exp_vals = self.expectation_value(psi)
        
        max_key = max(exp_vals, key=lambda k: abs(exp_vals[k]))
        
        guna_map = {
            "sattva": GunaType.SATTVA,
            "rajas": GunaType.RAJAS,
            "tamas": GunaType.TAMAS,
        }
        
        return guna_map[max_key]

