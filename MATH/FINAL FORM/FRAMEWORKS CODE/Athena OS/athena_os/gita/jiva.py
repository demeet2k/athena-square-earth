@dataclass
class ActionVector:
    """An action with its properties."""
    
    action_id: int
    name: str
    guna_type: ActionGuna
    dharma_value: float      # Alignment with cosmic law (-1 to 1)
    pleasure_value: float    # Sensory gratification (0 to 1)
    karma_weight: float = 1.0  # How much karma it generates
    
    @property
    def resultant_karma(self) -> float:
        """
        Calculate karma generated by this action.
        
        Sattvic actions generate less binding karma.
        Tamasic actions generate heavy karma.
        """
        guna_factor = {
            ActionGuna.SATTVIC: 0.1,
            ActionGuna.RAJASIC: 1.0,
            ActionGuna.TAMASIC: 2.0,
        }
        
        return self.karma_weight * guna_factor[self.guna_type]

