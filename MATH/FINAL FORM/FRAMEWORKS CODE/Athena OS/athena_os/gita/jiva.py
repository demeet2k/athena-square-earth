@dataclass
class Citta:
    """
    The Citta: Database of Vāsanās (latent impressions).
    
    Implemented as a weight matrix W[s][a] representing the
    tendency to take action a in state s.
    
    In RL terms: the policy network weights.
    """
    
    num_states: int = 16
    num_actions: int = 4
    
    # Weight matrix (Vāsanā storage)
    weights: np.ndarray = field(default=None, repr=False)
    
    # Learning rate for updating weights
    learning_rate: float = 0.1
    
    def __post_init__(self):
        if self.weights is None:
            # Initialize with small random weights
            self.weights = np.random.randn(self.num_states, self.num_actions) * 0.1
    
    def get_action_probabilities(self, state: int) -> np.ndarray:
        """
        Get probability distribution over actions for a state.
        
        Uses softmax over weights.
        """
        logits = self.weights[state, :]
        exp_logits = np.exp(logits - np.max(logits))  # Stability
        return exp_logits / np.sum(exp_logits)
    
    def select_action(self, state: int) -> int:
        """Sample an action based on current Vāsanās."""
        probs = self.get_action_probabilities(state)
        return int(np.random.choice(self.num_actions, p=probs))
    
    def update_vasana(self, state: int, action: int, 
                      reward: float) -> None:
        """
        Update Vāsanā weights based on experience.
        
        Positive reward strengthens the tendency.
        """
        self.weights[state, action] += self.learning_rate * reward
    
    def vasana_energy(self) -> float:
        """Total energy stored in Vāsanās."""
        return float(np.linalg.norm(self.weights))
    
    def clear_vasanas(self) -> None:
        """Purge all latent impressions (deep meditation)."""
        self.weights.fill(0)
    
    def is_sattvic(self) -> bool:
        """
        Check if Citta is predominantly Sattvic.
        
        Low total Vāsanā energy indicates purity.
        """
        return self.vasana_energy() < 0.1

