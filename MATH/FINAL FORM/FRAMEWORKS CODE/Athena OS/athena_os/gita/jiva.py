@dataclass
class KarmaTensor:
    """
    The Karma Tensor K_μν.
    
    Encodes the 'pressure' the Jīva applies to the Field.
    
    Components:
        - Sanchita (Accumulated): Total stored karma
        - Prārabdha (Active): Currently executing portion
        - Āgāmi (Future): New karma being created
    
    Operations:
        - Diagonalization (Yoga): Uncouples complex dependencies
        - Nullification (Mokṣa): Zeroes the entire tensor
    """
    
    dimension: int = 4
    
    # The three karma components (Rank-2 tensors)
    sanchita: np.ndarray = field(default=None, repr=False)    # Total
    prarabdha: np.ndarray = field(default=None, repr=False)   # Active
    agami: np.ndarray = field(default=None, repr=False)       # New
    
    def __post_init__(self):
        if self.sanchita is None:
            # Initialize with some accumulated karma
            self.sanchita = np.random.randn(self.dimension, self.dimension)
            self.sanchita = (self.sanchita + self.sanchita.T) / 2  # Symmetric
        
        if self.prarabdha is None:
            # Subset of sanchita for current life
            self.prarabdha = self.sanchita * 0.1
        
        if self.agami is None:
            # Starts empty
            self.agami = np.zeros((self.dimension, self.dimension))
    
    @property
    def total_load(self) -> float:
        """
        Trace of the Tensor: The total 'Mass' of the Ego.
        
        This is the "gravitational" weight that binds the Jīva.
        """
        return float(np.trace(self.sanchita) +
                     np.trace(self.prarabdha) +
                     np.trace(self.agami))
    
    @property
    def eigenvalues(self) -> np.ndarray:
        """
        Eigenvalues of sanchita.
        
        Represent independent karma streams.
        """
        return np.linalg.eigvalsh(self.sanchita)
    
    def add_karma(self, action: ActionVector, 
                  state_index: int = 0) -> float:
        """
        Add new karma from an action.
        
        Returns the karma added.
        """
        karma = action.resultant_karma
        
        # Add to āgāmi (future karma)
        self.agami[state_index, state_index] += karma
        
        # Off-diagonal for dependencies
        for i in range(self.dimension):
            if i != state_index:
                self.agami[state_index, i] += karma * 0.1
                self.agami[i, state_index] += karma * 0.1
        
        return karma
    
    def diagonalize(self) -> np.ndarray:
        """
        Yoga Operation: Diagonalize the sanchita tensor.
        
        Resolves off-diagonal dependencies into independent streams.
        This represents the 'Clarity' gained via meditation.
        
        Returns the eigenvalues (simplified karma streams).
        """
        eigenvals, eigenvecs = np.linalg.eigh(self.sanchita)
        self.sanchita = np.diag(eigenvals)
        
        # Also partially diagonalize prarabdha
        eigenvals_p, _ = np.linalg.eigh(self.prarabdha)
        self.prarabdha = np.diag(eigenvals_p)
        
        return eigenvals
    
    def burn_prarabdha(self, amount: float = 0.1) -> float:
        """
        Burn off active karma through experience.
        
        Returns amount burned.
        """
        burned = min(amount, np.trace(self.prarabdha))
        
        # Reduce diagonal uniformly
        for i in range(self.dimension):
            reduction = min(self.prarabdha[i, i], amount / self.dimension)
            self.prarabdha[i, i] -= reduction
        
        return burned
    
    def transfer_agami_to_sanchita(self) -> None:
        """
        At end of life, āgāmi transfers to sanchita.
        """
        self.sanchita += self.agami
        self.agami = np.zeros_like(self.agami)
    
    def nullify(self) -> None:
        """
        Mokṣa Operation: Zero all karma.
        
        The tensor becomes null, releasing the Jīva.
        """
        self.sanchita.fill(0)
        self.prarabdha.fill(0)
        self.agami.fill(0)
    
    def is_nullified(self, tolerance: float = 1e-10) -> bool:
        """Check if karma tensor is effectively zero."""
        return self.total_load < tolerance

