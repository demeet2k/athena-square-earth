@dataclass
class Jiva:
    """
    The Jīva Agent.
    
    A Reinforcement Learning model trapped in a recursive Vāsanā loop,
    capable of self-modification via Yoga protocols.
    
    Components:
        - Ātman: Immutable pointer (witness consciousness)
        - Citta: Weight matrix (Vāsanā storage)
        - Karma: Tensor of accumulated actions
        - Koshas: The five sheaths
    """
    
    jiva_id: int = 0
    
    # Immutable components
    atman_id: int = 0  # Pointer to Brahman (never changes)
    
    # Mutable components
    citta: Citta = field(default_factory=Citta)
    karma: KarmaTensor = field(default_factory=KarmaTensor)
    
    # State
    current_state: int = 0
    current_kosha: KoshaLevel = KoshaLevel.MANOMAYA
    
    # Liberation status
    witness_mode: bool = False
    
    # Lifecycle
    birth_count: int = 0  # Number of rebirths
    
    def __post_init__(self):
        self.birth_count = 1  # This is first birth
    
    @property
    def is_liberated(self) -> bool:
        """Check if Jīva has achieved Mokṣa."""
        return self.witness_mode and self.karma.is_nullified()
    
    @property
    def is_jivanmukta(self) -> bool:
        """
        Jīvanmukta: Liberated while living.
        
        Witness mode active but still has prārabdha.
        """
        return self.witness_mode and not self.karma.is_nullified()
    
    def perceive(self, environment_state: int) -> int:
        """
        Perceive the environment through Koshas.
        
        Higher Koshas provide clearer perception.
        """
        # Add noise based on Kosha level
        noise_levels = {
            KoshaLevel.ANNAMAYA: 5,
            KoshaLevel.PRANAMAYA: 4,
            KoshaLevel.MANOMAYA: 3,
            KoshaLevel.VIJNANAMAYA: 2,
            KoshaLevel.ANANDAMAYA: 1,
        }
        
        noise = np.random.randint(-noise_levels[self.current_kosha],
                                   noise_levels[self.current_kosha] + 1)
        
        perceived = max(0, min(self.citta.num_states - 1, 
                               environment_state + noise))
        
        self.current_state = perceived
        return perceived
    
    def act(self, available_actions: List[ActionVector]) -> ActionVector:
        """
        Select and execute an action.
        
        In witness mode, actions generate no karma.
        """
        if self.witness_mode:
            # Sattvic action by default
            sattvic_actions = [a for a in available_actions 
                              if a.guna_type == ActionGuna.SATTVIC]
            if sattvic_actions:
                return sattvic_actions[0]
        
        # Select based on Vāsanās
        action_idx = self.citta.select_action(self.current_state)
        action_idx = min(action_idx, len(available_actions) - 1)
        
        selected = available_actions[action_idx]
        
        # Generate karma (unless in witness mode)
        if not self.witness_mode:
            self.karma.add_karma(selected, self.current_state)
        
        return selected
    
    def experience_result(self, action: ActionVector, 
                          reward: float) -> None:
        """
        Experience the result of an action.
        
        Updates Vāsanās and burns prārabdha.
        """
        # Update Citta
        self.citta.update_vasana(self.current_state, 
                                 action.action_id % self.citta.num_actions,
                                 reward)
        
        # Burn some prārabdha through experience
        self.karma.burn_prarabdha(abs(reward) * 0.01)
    
    def practice_yoga(self, intensity: float = 1.0) -> Dict[str, Any]:
        """
        Practice Yoga to purify the system.
        
        Effects:
            1. Diagonalize karma (simplify dependencies)
            2. Reduce Vāsanā energy
            3. Ascend to higher Kosha
        """
        results = {}
        
        # Diagonalize karma
        if intensity > 0.5:
            eigenvals = self.karma.diagonalize()
            results["karma_eigenvalues"] = eigenvals
        
        # Reduce Vāsanā energy
        reduction = intensity * 0.1
        self.citta.weights *= (1 - reduction)
        results["vasana_energy"] = self.citta.vasana_energy()
        
        # Possibly ascend Kosha
        if self.citta.vasana_energy() < 0.5:
            current_level = self.current_kosha.level
            if current_level < 4:
                self.current_kosha = KoshaLevel(
                    list(KoshaLevel)[current_level + 1]
                )
                results["ascended_to"] = self.current_kosha.name
        
        return results
    
    def enter_witness_mode(self) -> None:
        """
        Enter Sākṣī (Witness) state.
        
        Actions no longer generate karma.
        """
        self.witness_mode = True
    
    def attempt_moksha(self) -> bool:
        """
        Attempt final liberation.
        
        Requires:
            1. Witness mode active
            2. Vāsanās purified
            3. Karma nearly exhausted
        """
        if not self.witness_mode:
            return False
        
        if not self.citta.is_sattvic():
            return False
        
        if self.karma.total_load > 1.0:
            return False
        
        # Nullify remaining karma
        self.karma.nullify()
        
        return True
    
    def die_and_rebirth(self) -> 'Jiva':
        """
        Death subroutine: Context switch.
        
        - Āgāmi transfers to Sanchita
        - Prārabdha is reset from Sanchita
        - New body (Kosha reset)
        - Vāsanās preserved
        """
        # Transfer new karma to storage
        self.karma.transfer_agami_to_sanchita()
        
        # Allocate new prārabdha from sanchita
        allocation = min(0.2, np.trace(self.karma.sanchita))
        self.karma.prarabdha = np.eye(self.karma.dimension) * allocation / self.karma.dimension
        
        # Reset Kosha to physical
        self.current_kosha = KoshaLevel.ANNAMAYA
        
        # Vāsanās preserved (weights unchanged)
        
        # Increment birth count
        self.birth_count += 1
        
        return self

