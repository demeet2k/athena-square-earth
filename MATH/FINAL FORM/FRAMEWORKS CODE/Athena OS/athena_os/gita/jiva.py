@dataclass
class DashaSystem:
    """
    The Cosmic Clock: Vimśottari Dasha System.
    
    Determines which Planetary Operator is active at any time.
    Each planet represents a specific mode of karma processing.
    """
    
    # Mahādashā periods in years
    PERIODS: Dict[str, int] = field(default_factory=lambda: {
        'KETU': 7, 'VENUS': 20, 'SUN': 6, 'MOON': 10, 'MARS': 7,
        'RAHU': 18, 'JUPITER': 16, 'SATURN': 19, 'MERCURY': 17
    })
    
    SEQUENCE: List[str] = field(default_factory=lambda: [
        'KETU', 'VENUS', 'SUN', 'MOON', 'MARS', 
        'RAHU', 'JUPITER', 'SATURN', 'MERCURY'
    ])
    
    # Total cycle length
    TOTAL_CYCLE: int = 120  # years
    
    # Current position in cycle
    current_year: float = 0.0
    
    # Birth position (determines initial dasha)
    birth_nakshatra_position: float = 0.0  # 0 to 1
    
    def __post_init__(self):
        # Calculate starting position based on birth
        total_so_far = 0
        for planet in self.SEQUENCE:
            total_so_far += self.PERIODS[planet]
        
        self.TOTAL_CYCLE = total_so_far
    
    def get_current_dasha(self) -> Tuple[str, float]:
        """
        Get current Mahādashā and phase within it.
        
        Returns (planet_name, phase_0_to_1)
        """
        # Offset by birth position
        adjusted_year = (self.current_year + 
                         self.birth_nakshatra_position * self.TOTAL_CYCLE)
        
        # Wrap around cycle
        cycle_position = adjusted_year % self.TOTAL_CYCLE
        
        # Find current dasha
        accumulated = 0
        for planet in self.SEQUENCE:
            period = self.PERIODS[planet]
            if accumulated + period > cycle_position:
                phase = (cycle_position - accumulated) / period
                return (planet, phase)
            accumulated += period
        
        return (self.SEQUENCE[0], 0.0)
    
    def get_planetary_operator(self) -> np.ndarray:
        """
        Get the Hamiltonian modifier for current dasha.
        
        Each planet has a characteristic operator.
        """
        planet, phase = self.get_current_dasha()
        
        # Simplified 4x4 operators for each planet
        operators = {
            'SUN': np.diag([1, 0.8, 0.6, 0.4]),      # Fire, authority
            'MOON': np.diag([0.5, 1, 0.5, 0.8]),     # Mind, fluctuation
            'MARS': np.diag([0.9, 0.9, 1, 0.3]),     # Energy, conflict
            'MERCURY': np.diag([0.7, 0.7, 0.7, 1]),  # Intellect, communication
            'JUPITER': np.diag([1, 1, 0.8, 0.9]),    # Expansion, wisdom
            'VENUS': np.diag([0.6, 1, 1, 0.7]),      # Pleasure, beauty
            'SATURN': np.diag([0.3, 0.4, 0.5, 1]),   # Restriction, karma
            'RAHU': np.diag([0.8, 0.6, 0.4, 0.9]),   # Obsession, shadow
            'KETU': np.diag([0.2, 0.3, 0.8, 0.5]),   # Liberation, detachment
        }
        
        return operators.get(planet, np.eye(4))
    
    def advance(self, years: float) -> str:
        """Advance time and return current dasha lord."""
        self.current_year += years
        planet, _ = self.get_current_dasha()
        return planet
    
    def apply_to_jiva(self, jiva: Jiva, dt: float = 0.1) -> None:
        """
        Apply dasha influence to a Jīva.
        
        Modulates karma processing rate.
        """
        operator = self.get_planetary_operator()
        
        # Modulate prārabdha burning rate
        planet, phase = self.get_current_dasha()
        
        # Saturn and Rahu slow karma burning
        if planet in ['SATURN', 'RAHU']:
            jiva.karma.burn_prarabdha(dt * 0.5)
        # Jupiter and Venus accelerate it
        elif planet in ['JUPITER', 'VENUS']:
            jiva.karma.burn_prarabdha(dt * 1.5)
        else:
            jiva.karma.burn_prarabdha(dt)

