def neg(x: B4) -> B4:
    """
    Truth-negation (bit-flip): ¬x := {1-b | b ∈ x}
    
    ¬0 = 1, ¬1 = 0, ¬⊥ = ⊥, ¬⊤ = ⊤
    """
    flipped = frozenset(1 - b for b in x.support)
    return B4.from_support(flipped)

def kappa(x: B4) -> B4:
    """
    Conflation / shadow swap: κ
    
    κ(⊥) = ⊤, κ(⊤) = ⊥, κ(0) = 0, κ(1) = 1
    
    Swaps gap and conflict while preserving classical values.
    """
    if x == B4.BOT:
        return B4.TOP
    elif x == B4.TOP:
        return B4.BOT
    else:
        return x

def complement(x: B4) -> B4:
    """
    Set complement: ~x := {0,1} \ x
    
    ~⊥ = ⊤, ~⊤ = ⊥, ~0 = 1, ~1 = 0
    """
    full = frozenset({0, 1})
    return B4.from_support(full - x.support)

def identity(x: B4) -> B4:
    """Identity function: id(x) = x"""
    return x

# Aliases
NOT = neg            # ¬
SWAP = kappa         # κ
COMP = complement    # ~
ID = identity        # id

