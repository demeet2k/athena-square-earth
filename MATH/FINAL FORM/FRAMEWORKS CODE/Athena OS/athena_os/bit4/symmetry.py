# KLEIN FOUR GROUP
# =============================================================================

class KleinElement(Enum):
    """Elements of the Klein four group V₄."""
    
    ID = "id"      # Identity
    NEG = "neg"    # Truth-negation ¬
    KAPPA = "kappa"  # Conflation κ
    COMP = "comp"  # Set complement ~

# Group operation table for V₄
# Composition: a ∘ b means apply b first, then a
KLEIN_TABLE: Dict[Tuple[KleinElement, KleinElement], KleinElement] = {
    # id ∘ x = x
    (KleinElement.ID, KleinElement.ID): KleinElement.ID,
    (KleinElement.ID, KleinElement.NEG): KleinElement.NEG,
    (KleinElement.ID, KleinElement.KAPPA): KleinElement.KAPPA,
    (KleinElement.ID, KleinElement.COMP): KleinElement.COMP,
    
    # ¬ ∘ x
    (KleinElement.NEG, KleinElement.ID): KleinElement.NEG,
    (KleinElement.NEG, KleinElement.NEG): KleinElement.ID,
    (KleinElement.NEG, KleinElement.KAPPA): KleinElement.COMP,
    (KleinElement.NEG, KleinElement.COMP): KleinElement.KAPPA,
    
    # κ ∘ x
    (KleinElement.KAPPA, KleinElement.ID): KleinElement.KAPPA,
    (KleinElement.KAPPA, KleinElement.NEG): KleinElement.COMP,
    (KleinElement.KAPPA, KleinElement.KAPPA): KleinElement.ID,
    (KleinElement.KAPPA, KleinElement.COMP): KleinElement.NEG,
    
    # ~ ∘ x
    (KleinElement.COMP, KleinElement.ID): KleinElement.COMP,
    (KleinElement.COMP, KleinElement.NEG): KleinElement.KAPPA,
    (KleinElement.COMP, KleinElement.KAPPA): KleinElement.NEG,
    (KleinElement.COMP, KleinElement.COMP): KleinElement.ID,
}

def klein_compose(a: KleinElement, b: KleinElement) -> KleinElement:
    """Compose two Klein group elements: a ∘ b (apply b first)."""
    return KLEIN_TABLE[(a, b)]

def klein_inverse(a: KleinElement) -> KleinElement:
    """
    Inverse of Klein group element.
    
    Every element is its own inverse (all involutions).
    """
    return a

def klein_apply(elem: KleinElement, x: B4) -> B4:
    """Apply Klein group element as B4 transformation."""
    funcs = {
        KleinElement.ID: identity,
        KleinElement.NEG: neg,
        KleinElement.KAPPA: kappa,
        KleinElement.COMP: complement
    }
    return funcs[elem](x)

