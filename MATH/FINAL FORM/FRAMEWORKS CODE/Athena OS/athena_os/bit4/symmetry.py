@dataclass
class DihedralElement:
    """
    Element of D₄ acting on B₄.
    
    D₄ is generated by V₄ plus the rotation r: (⊥ → 0 → ⊤ → 1 → ⊥)
    """
    
    klein: KleinElement
    rotation: int  # 0, 1, 2, or 3 (mod 4)
    
    def apply(self, x: B4) -> B4:
        """Apply D₄ element to B4 value."""
        # First apply Klein component
        result = klein_apply(self.klein, x)
        
        # Then apply rotation
        # Rotation order: ⊥ → 0 → ⊤ → 1 → ⊥
        cycle = [B4.BOT, B4.ZERO, B4.TOP, B4.ONE]
        idx = cycle.index(result)
        rotated_idx = (idx + self.rotation) % 4
        
        return cycle[rotated_idx]

