def print_involution_table() -> str:
    """Generate involution operation tables."""
    values = [B4.BOT, B4.ZERO, B4.ONE, B4.TOP]
    
    lines = ["Involution Tables:"]
    lines.append("")
    
    # Negation
    lines.append("¬ (negation):")
    for x in values:
        lines.append(f"  ¬{x.glyph} = {neg(x).glyph}")
    lines.append("")
    
    # Kappa
    lines.append("κ (conflation):")
    for x in values:
        lines.append(f"  κ({x.glyph}) = {kappa(x).glyph}")
    lines.append("")
    
    # Complement
    lines.append("~ (complement):")
    for x in values:
        lines.append(f"  ~{x.glyph} = {complement(x).glyph}")
    
    return "\n".join(lines)

def print_klein_table() -> str:
    """Generate Klein group composition table."""
    elements = [KleinElement.ID, KleinElement.NEG, KleinElement.KAPPA, KleinElement.COMP]
    names = {"id": "id", "neg": "¬", "kappa": "κ", "comp": "~"}
    
    header = "∘  | " + " ".join(f"{names[e.value]:2}" for e in elements)
    sep = "-" * len(header)
    
    rows = []
    for a in elements:
        cells = [names[klein_compose(a, b).value] for b in elements]
        row = f"{names[a.value]:2} | " + " ".join(f"{c:2}" for c in cells)
        rows.append(row)
    
    return header + "\n" + sep + "\n" + "\n".join(rows)

