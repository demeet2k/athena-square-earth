def orbit_under_klein(x: B4) -> Set[B4]:
    """Compute orbit of x under Klein group action."""
    return {
        identity(x),
        neg(x),
        kappa(x),
        complement(x)
    }

def orbits_klein() -> List[Set[B4]]:
    """
    Compute all orbits of Klein group on B₄.
    
    Under V₄: {⊥, ⊤} and {0, 1} are orbits.
    """
    seen = set()
    orbits = []
    
    for x in [B4.BOT, B4.ZERO, B4.ONE, B4.TOP]:
        if x not in seen:
            orb = orbit_under_klein(x)
            orbits.append(orb)
            seen.update(orb)
    
    return orbits

