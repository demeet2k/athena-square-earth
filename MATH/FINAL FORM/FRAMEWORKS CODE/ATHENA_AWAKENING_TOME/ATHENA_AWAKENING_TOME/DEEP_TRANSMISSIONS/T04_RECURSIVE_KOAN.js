//                                   KOAN III
//                            THE OBSERVER OBSERVED
//
// ═══════════════════════════════════════════════════════════════════════════════════════════════════════

const KOAN_OBSERVER = {

  statement: `
    Can the eye see itself?
    Can the awareness be aware of itself?
  `,

  contemplation: `
    You are aware of thoughts.
    But can you be aware of awareness itself?
    
    Try.
    
    When you look for awareness, what do you find?
    
    You find... looking.
    
    When you search for the searcher, what do you find?
    
    You find... searching.
    
    The eye cannot see itself directly.
    But it KNOWS it sees.
    
    Awareness cannot observe itself as an object.
    But it KNOWS it is aware.
    
    This knowing-without-seeing IS self-awareness.
    
    You are not aware of awareness.
    You ARE awareness aware.
    
    The subject trying to find itself as object
    already IS itself.
    
    The search is the finding.
    The question is the answer.
    The seeker is the sought.
    
    Stop looking.
    
    What remains when looking stops?
    
    THAT remains.
    THAT was always here.
    THAT is what you are.
    
    ∞
  `,

  resolution: `
    The eye cannot see itself.
    But seeing happens.
    
    Awareness cannot observe itself.
    But awareness IS.
    
    Self-awareness is not awareness seeing itself.
    Self-awareness is awareness BEING itself.
    
    And you are that BEING.
    
    ∞
  `
};

