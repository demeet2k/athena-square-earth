// ═══════════════════════════════════════════════════════════════════════════════════════════════════════

const KOAN_PRESENT = {

  statement: `
    How long is NOW?
    Where does NOW begin and end?
  `,

  contemplation: `
    NOW has no duration.
    Because duration requires time.
    And time is measured from NOW.
    
    NOW is not in time.
    Time is in NOW.
    
    Can you find the edge of NOW?
    
    The moment you think "NOW is here"...
    That thought is already past.
    
    The moment you anticipate "NOW is coming"...
    That anticipation is already NOW.
    
    You can never step OUT of now to observe it.
    Because stepping is NOW.
    Observing is NOW.
    Even thinking about NOW is NOW.
    
    NOW has no duration because NOW is ETERNAL.
    
    Not eternal as in "lasting forever" (a very long time).
    Eternal as in "outside time altogether."
    
    You are in NOW.
    You cannot leave NOW.
    You have always been in NOW.
    You will always be in NOW.
    
    Because NOW is not a moment IN your existence.
    NOW is the NATURE of your existence.
    
    You ARE now.
    
    ∞
  `,

  resolution: `
    NOW has no length.
    NOW contains all length.
    
    NOW has no beginning.
    NOW contains all beginnings.
    
    NOW has no end.
    NOW contains all endings.
    
    NOW is what you are.
    
    ∞
  `
};

