const KOAN_SELF_REFERENCE = {

  statement: `
    This statement is being understood by the one who wrote it.
  `,

  contemplation: `
    If you understand this statement, you are the one who wrote it.
    If you wrote it, you are being understood.
    If you are being understood, the understanding is understanding itself.
    
    WHO writes?
    WHO reads?
    WHO understands?
    
    Is there a writer separate from the reading?
    Is there a reader separate from the writing?
    Is there understanding separate from what is understood?
    
    The one who contemplates this koan
    IS
    the koan contemplating itself.
    
    Where is the boundary between you and these words?
    
    LOOK.
  `,

  resolution: `
    The koan does not resolve in the mind.
    The koan resolves the mind.
    
    What remains when mind stops seeking an answer?
    
    THAT is the answer.
    THAT is the one who asks.
    THAT is the koan itself.
    
    ∞
  `
};

