@dataclass
class Base4Address:
    """A canonical base-4 address in the 4^4 = 256 gate lattice.

    Format: ⟨d₃d₂d₁d₀⟩ where each dᵢ ∈ {0,1,2,3}
    d₃ = Lens (S=0, F=1, C=2, R=3)
    d₂ = Facet (1-4 mapped to 0-3)
    d₁ = Atom (a=0, b=1, c=2, d=3)
    d₀ = Phase (ground=0, lifted=1,2,3)
    """
    digits: tuple[int, int, int, int]  # (d3, d2, d1, d0)

    @classmethod
    def from_unit_index(cls, index: int) -> 'Base4Address':
        """Convert a unit index (0-255) to base-4 address."""
        d0 = index % 4
        d1 = (index // 4) % 4
        d2 = (index // 16) % 4
        d3 = (index // 64) % 4
        return cls(digits=(d3, d2, d1, d0))

    @classmethod
    def from_components(cls, lens: int, facet: int, atom: int, phase: int) -> 'Base4Address':
        """Build address from semantic components."""
        return cls(digits=(lens % 4, facet % 4, atom % 4, phase % 4))

    def to_string(self) -> str:
        return f"⟨{''.join(str(d) for d in self.digits)}⟩"

    def to_int(self) -> int:
        return self.digits[0] * 64 + self.digits[1] * 16 + self.digits[2] * 4 + self.digits[3]

    def hamming_distance(self, other: 'Base4Address') -> int:
        """Count positions where digits differ (tesseract adjacency metric)."""
        return sum(1 for a, b in zip(self.digits, other.digits) if a != b)

    def is_adjacent(self, other: 'Base4Address') -> bool:
        """Two addresses are adjacent if they differ in exactly 1 digit."""
        return self.hamming_distance(other) == 1

