class Hologram4DCompressor:
    """Compresses the full 5D manuscript expansion into base-4 holographic seeds.

    Process:
        1. Assign each unit a base-4 address in the 4^4 = 256 gate lattice
        2. Build the crystal tile (S/F/C/R × 4 facets × 4 atoms)
        3. Assign odd-dimensional orbit state from the 60-artifact system
        4. Compute tunneling adjacency as tesseract adjacency
        5. Generate TCoordΩ routing coordinate
        6. Collapse through Z* for verification hash
        7. Produce the compressed seed
    """

    def __init__(self):
        self.artifacts = _build_full_60_artifacts()
        self.artifact_map = {a.number: a for a in self.artifacts}
        self.seeds: list[CompressedSeed] = []
        self.tunnel_matrix: dict[str, list[str]] = {}

    def _assign_artifact(self, unit: ManuscriptUnit) -> SymmetryArtifact:
        """Assign a quaternionic artifact from I₆₀ to a manuscript unit."""
        # Map unit index to artifact number (1-60, wrapping)
        art_n = (unit.index % 60) + 1
        # Shift by kind to distribute across classes
        if unit.kind == UnitKind.CHAPTER:
            art_n = (unit.index % 24) + 2       # Pentad range 2-25
        elif unit.kind == UnitKind.APPENDIX:
            art_n = (unit.index % 20) + 26      # Triad range 26-45
        elif unit.kind in (UnitKind.EMERGENT, UnitKind.METRO_NUCLEUS):
            art_n = (unit.index % 15) + 46      # Möbius range 46-60
        else:  # Reverse
            art_n = (unit.index % 24) + 2       # Pentad (mirrored)

        if art_n in self.artifact_map:
            return self.artifact_map[art_n]
        return self.artifact_map[1]  # Fallback to identity

    def _build_crystal_tile(self, unit: ManuscriptUnit, base4: Base4Address) -> CrystalTile:
        """Build the 4^4 crystal tile for a unit."""
        tile = CrystalTile(unit_code=unit.code, base4_addr=base4)

        for lens in Lens:
            for facet in Facet:
                for atom in Atom:
                    addr = f"{unit.code}{base4.to_string()}.{lens.name}{facet.value}.{atom.name.lower()}"
                    content = f"{unit.code}:{lens.name}:{facet.value}:{atom.name}"
                    content_hash = hashlib.sha256(content.encode()).hexdigest()[:12]
                    crystal_atom = CrystalAtom(
                        address=addr,
                        content_hash=content_hash,
                        lens=lens,
                        facet=facet,
                        atom=atom,
                        status="ACTIVE" if lens == Lens.S or (lens == Lens.F and facet.value <= 2) else "STUB",
                    )
                    tile.atoms[addr] = crystal_atom

        return tile

    def _compute_tunnels(self, unit: ManuscriptUnit, base4: Base4Address) -> list[str]:
        """Compute tunnel adjacency for a unit.

        Tunnels are derived from:
        1. Sequential chapter order (Ch_n → Ch_{n+1})
        2. Arc grouping (3 chapters per arc)
        3. Rail coupling (Su/Me/Sa modular)
        4. Appendix coupling (chapter → related appendix)
        5. Legacy → Emergent (arc → E chapter)
        6. Emergent → Reverse (E → Rev mirror)
        7. Möbius bridges (Q ingress, O return)
        8. Tesseract adjacency (Hamming distance = 1 in base-4)
        """
        tunnels = []

        if unit.kind == UnitKind.CHAPTER:
            # Sequential
            if unit.index < 20:
                tunnels.append(f"Ch{unit.index + 2:02d}")
            if unit.index > 0:
                tunnels.append(f"Ch{unit.index:02d}")
            # Arc grouping: 3 chapters per arc (0-2, 3-5, 6-8, ...)
            arc = unit.index // 3
            arc_start = arc * 3
            for i in range(arc_start, min(arc_start + 3, 21)):
                code = f"Ch{i + 1:02d}"
                if code != unit.code and code not in tunnels:
                    tunnels.append(code)
            # Appendix coupling
            app_idx = unit.index % 16
            app_letters = "ABCDEFGHIJKLMNOP"
            tunnels.append(f"App{app_letters[app_idx]}")
            # Legacy → Emergent
            e_idx = min(unit.index // 3, 8)  # Arc → E chapter (0-6 for E01-E07)
            tunnels.append(f"E{e_idx + 1:02d}")
            # Ch11 special: tunnels to Ch01 (the great loop)
            if unit.index == 10:
                tunnels.append("Ch01")
            # Ch21 special: tunnels to Ch11 and Ch01
            if unit.index == 20:
                if "Ch11" not in tunnels:
                    tunnels.append("Ch11")
                if "Ch01" not in tunnels:
                    tunnels.append("Ch01")

        elif unit.kind == UnitKind.APPENDIX:
            # Ring adjacency
            app_letters = "ABCDEFGHIJKLMNOP"
            if unit.index > 0:
                tunnels.append(f"App{app_letters[unit.index - 1]}")
            if unit.index < 15:
                tunnels.append(f"App{app_letters[unit.index + 1]}")
            # Mirror reverse
            rev_idx = 15 - unit.index
            tunnels.append(REVERSE_APPENDICES[rev_idx].code)
            # Chapter coupling
            ch_idx = unit.index % 21
            tunnels.append(f"Ch{ch_idx + 1:02d}")

        elif unit.kind in (UnitKind.EMERGENT, UnitKind.METRO_NUCLEUS):
            # Emergent orbit
            if unit.index > 0 and unit.index <= 9:
                tunnels.append(f"E{unit.index:02d}")
            if unit.index < 9:
                tunnels.append(f"E{unit.index + 2:02d}")
            # Back to legacy arc
            if unit.index < 7:
                arc_start = unit.index * 3
                for i in range(arc_start, min(arc_start + 3, 21)):
                    tunnels.append(f"Ch{i + 1:02d}")
            # E09 → Z* → everything
            if unit.index == 8:
                tunnels.extend(["E01", "E04", "Ch01", "Ch11"])
            # E10 → all emergent
            if unit.index == 9:
                tunnels.extend([f"E{i:02d}" for i in range(1, 10)])

        elif unit.kind == UnitKind.REVERSE_APPENDIX:
            # Reverse ring
            if unit.index > 0:
                tunnels.append(REVERSE_APPENDICES[unit.index - 1].code)
            if unit.index < 15:
                tunnels.append(REVERSE_APPENDICES[unit.index + 1].code)
            # Back to emergent
            e_idx = unit.index % 10
            tunnels.append(f"E{e_idx + 1:02d}" if e_idx < 10 else "E10")
            # Mirror legacy appendix
            app_letters = "ABCDEFGHIJKLMNOP"
            mirror_idx = 15 - unit.index
            tunnels.append(f"App{app_letters[mirror_idx]}")
            # Q ingress (index 9) and O return (index 11)
            if unit.index == 9:  # Q
                tunnels.extend(["E02", "E06", "AppP_rev"])
            if unit.index == 11:  # O
                tunnels.extend(["E01", "E07", "AppN_rev"])

        # Deduplicate
        seen = set()
        unique = []
        for t in tunnels:
            if t not in seen and t != unit.code:
                seen.add(t)
                unique.append(t)
        return unique

