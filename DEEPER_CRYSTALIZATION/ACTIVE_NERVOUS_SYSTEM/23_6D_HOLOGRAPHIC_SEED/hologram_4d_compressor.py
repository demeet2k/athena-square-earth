    petal_count: int = 5              # Sacred geometry petal count
    interference_order: int = 1       # Harmonic order
    return_mode: str = "HELICAL"      # HELICAL | FLAT | MOBIUS

    def weave_operator(self, target_q: Quaternion) -> Quaternion:
        """WeaveΩ(Target; δ, σ, ψ) := Project₄D(OrbitOdd(Target; δ, ψ) through σ)

        Rotates target through odd-dimensional orbit and projects back to 4D.
        """
        # Apply the artifact quaternion as conjugation: q·target·q̄
        q = self.quaternion
        q_conj = Quaternion(q.w, -q.x, -q.y, -q.z)
        # Quaternion multiplication: q * target * q_conjugate
        intermediate = q * target_q * q_conj
        return intermediate

    def collapse_to_z_star(self) -> str:
        """Collapse through Z* — strip to invariant hash."""
        data = f"{self.delta.value}:{self.weave.value}:{self.psi.value}:{self.quaternion}"
        return hashlib.sha256(data.encode()).hexdigest()[:12]

