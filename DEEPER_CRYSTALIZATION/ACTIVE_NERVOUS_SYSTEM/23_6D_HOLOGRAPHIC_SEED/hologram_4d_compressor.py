    def verify_metro_maps(self) -> dict[str, any]:
        """Verify that metro maps still work after compression.

        Checks:
        1. Every unit has a valid base-4 address
        2. Every tunnel connects to an existing unit
        3. The gamma corridor is intact
        4. Ch11 → Ch01 loop exists
        5. Ch21 → Ch11 loop exists
        6. Q → O bridge exists
        7. Every unit is reachable from Ch01
        """
        all_codes = {u.code for u in ALL_UNITS}
        results = {
            "total_seeds": len(self.seeds),
            "total_tunnels": sum(len(t) for t in self.tunnel_matrix.values()),
            "valid_addresses": 0,
            "broken_tunnels": [],
            "gamma_intact": False,
            "ch11_ch01_loop": False,
            "ch21_ch11_loop": False,
            "q_o_bridge": False,
            "reachable_from_ch01": 0,
        }

        # Check addresses
        for seed in self.seeds:
            gate = seed.base4.to_int()
            if 0 <= gate < 256:
                results["valid_addresses"] += 1

        # Check tunnel validity
        for code, tunnels in self.tunnel_matrix.items():
            for t in tunnels:
                if t not in all_codes:
                    results["broken_tunnels"].append(f"{code}→{t}")

        # Check gamma corridor
        gamma = [2, 11, 5, 14, 8, 2]
        gamma_exists = all(
            f"Ch{g:02d}" in all_codes or g <= 21 for g in gamma
        )
        results["gamma_intact"] = gamma_exists

        # Check loops
        results["ch11_ch01_loop"] = "Ch01" in self.tunnel_matrix.get("Ch11", [])
        results["ch21_ch11_loop"] = "Ch11" in self.tunnel_matrix.get("Ch21", [])

        # Check Möbius bridge
        q_tunnels = self.tunnel_matrix.get("AppQ_rev", [])
        o_tunnels = self.tunnel_matrix.get("AppO_rev", [])
        results["q_o_bridge"] = len(q_tunnels) > 0 and len(o_tunnels) > 0

        # BFS from Ch01
        visited = set()
        queue = ["Ch01"]
        while queue:
            current = queue.pop(0)
            if current in visited:
                continue
            visited.add(current)
            for t in self.tunnel_matrix.get(current, []):
                if t not in visited and t in all_codes:
                    queue.append(t)
        results["reachable_from_ch01"] = len(visited)

        return results

